package Self_Practice.freeCodeCamp;

class Operators {
    public static void main(String[] args){
        int num1 = 12;
        int num2 = 6;

        //addition
        System.out.println(num1 + num2);

        //Subtarction
        System.out.println(num1 - num2);

        //multiplication
        System.out.println(num1 * num2);

        //division
        System.out.println(num1/num2);

        //reminder
        System.out.println(num1%num2);
    }
}
